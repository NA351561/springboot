package com.wipro.springboot.model;

public class Family {
	
	private int mobileNo;
	private String firstName;
	private String lastName;
	private String dob;
	private String occupation;
	private double income;
	private double rent;
	private int noOfAdults;
	private int noOfChildren;
	private double adultExpenses;
	private double childExpenses;
	public int getMobileNo() {
		return mobileNo;
	}
	public void setMobileNo(int mobileNo) {
		this.mobileNo = mobileNo;
	}
	public String getFirstName() {
		return firstName;
	}
	public void setFirstName(String firstName) {
		this.firstName = firstName;
	}
	public String getLastName() {
		return lastName;
	}
	public void setLastName(String lastName) {
		this.lastName = lastName;
	}
	public String getDob() {
		return dob;
	}
	public void setDob(String dob) {
		this.dob = dob;
	}
	public String getOccupation() {
		return occupation;
	}
	public void setOccupation(String occupation) {
		this.occupation = occupation;
	}
	public double getIncome() {
		return income;
	}
	public void setIncome(double income) {
		this.income = income;
	}
	public double getRent() {
		return rent;
	}
	public void setRent(double rent) {
		this.rent = rent;
	}
	public int getNoOfAdults() {
		return noOfAdults;
	}
	public void setNoOfAdults(int noOfAdults) {
		this.noOfAdults = noOfAdults;
	}
	public int getNoOfChildren() {
		return noOfChildren;
	}
	public void setNoOfChildren(int noOfChildren) {
		this.noOfChildren = noOfChildren;
	}
	public double getAdultExpenses() {
		return adultExpenses;
	}
	public void setAdultExpenses(double adultExpenses) {
		this.adultExpenses = adultExpenses;
	}
	public double getChildExpenses() {
		return childExpenses;
	}
	public void setChildExpenses(double childExpenses) {
		this.childExpenses = childExpenses;
	}
	
	

}
